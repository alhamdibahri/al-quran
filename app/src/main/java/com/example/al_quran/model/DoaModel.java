package com.example.al_quran.model;

public class DoaModel {
    String id_doa, nama_doa, arab, latin, terjemahan;

    public String getId_doa() {
        return id_doa;
    }

    public void setId_doa(String id_doa) {
        this.id_doa = id_doa;
    }

    public String getNama_doa() {
        return nama_doa;
    }

    public void setNama_doa(String nama_doa) {
        this.nama_doa = nama_doa;
    }

    public String getArab() {
        return arab;
    }

    public void setArab(String arab) {
        this.arab = arab;
    }

    public String getLatin() {
        return latin;
    }

    public void setLatin(String latin) {
        this.latin = latin;
    }

    public String getTerjemahan() {
        return terjemahan;
    }

    public void setTerjemahan(String terjemahan) {
        this.terjemahan = terjemahan;
    }
}
